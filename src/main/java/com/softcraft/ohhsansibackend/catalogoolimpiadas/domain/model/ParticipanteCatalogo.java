package com.softcraft.ohhsansibackend.catalogoolimpiadas.domain.model;

public class ParticipanteCatalogo {
    private int idCategoria;
    private int idArea;
    private int idCatalogo;
    private int idOlimpiada;
    private int idParticipanteCatalogo;
    private int idInscripcion;
    private int idParticipante;


    public int getIdCategoria() {
        return idCategoria;
    }

    public void setIdCategoria(int idCategoria) {
        this.idCategoria = idCategoria;
    }

    public int getIdArea() {
        return idArea;
    }

    public void setIdArea(int idArea) {
        this.idArea = idArea;
    }

    public int getIdCatalogo() {
        return idCatalogo;
    }

    public void setIdCatalogo(int idCatalogo) {
        this.idCatalogo = idCatalogo;
    }

    public int getIdOlimpiada() {
        return idOlimpiada;
    }

    public void setIdOlimpiada(int idOlimpiada) {
        this.idOlimpiada = idOlimpiada;
    }

    public int getIdParticipanteCatalogo() {
        return idParticipanteCatalogo;
    }

    public void setIdParticipanteCatalogo(int idParticipanteCatalogo) {
        this.idParticipanteCatalogo = idParticipanteCatalogo;
    }

    public int getIdInscripcion() {
        return idInscripcion;
    }

    public void setIdInscripcion(int idInscripcion) {
        this.idInscripcion = idInscripcion;
    }

    public int getIdParticipante() {
        return idParticipante;
    }

    public void setIdParticipante(int idParticipante) {
        this.idParticipante = idParticipante;
    }
}
